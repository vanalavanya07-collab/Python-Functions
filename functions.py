def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def even_odd(n):
    if n % 2 == 0:
        return "Even"
    return "Odd"

def square(n):
    return n * n

print(add(10, 20))
print(subtract(30, 10))
print(multiply(5, 4))
print(even_odd(7))
print(square(6))